#include <iostream>
using namespace std;

const int ROWS = 8;
const int COLS = 8;

// 2차원 이미지에서 최대 화소 밝기를 반환하는 함수
int getMaxBrightness(int image[ROWS][COLS]) {
    int maxVal = image[0][0];
    for (int i = 0; i < ROWS; i++) {
        for (int j = 0; j < COLS; j++) {
            if (image[i][j] > maxVal)
                maxVal = image[i][j];
        }
    }
    return maxVal;
}

int main() {
    int image[ROWS][COLS] = {
        {120, 200, 150, 80,  60,  230, 170, 90},
        {45,  255, 130, 20,  180, 100, 75,  210},
        {190, 30,  220, 110, 50,  160, 240, 10},
        {70,  140, 85,  250, 35,  195, 55,  165},
        {215, 15,  175, 65,  145, 40,  235, 105},
        {95,  185, 25,  205, 115, 245, 5,   155},
        {135, 60,  225, 75,  170, 20,  200, 45},
        {80,  160, 50,  190, 100, 130, 70,  240}
    };

    int maxBrightness = getMaxBrightness(image);
    cout << "최대 화소 밝기: " << maxBrightness << endl;

    return 0;
}


