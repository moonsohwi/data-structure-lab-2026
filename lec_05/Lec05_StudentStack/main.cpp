// main.cpp  (슬라이드 p.30 교수님 테스트 코드 그대로)
#include "StudentStack.h"

int main() {
    StudentStack stack;                                          // 무명 객체

    stack.push(Student(2015130007, "홍길동", "컴퓨터공학과"));
    stack.push(Student(2015130100, "이순신", "기계공학과"));
    stack.push(Student(2015130135, "황희",   "법학과"));

    stack.display();    // [전체 학생의 수 =  3] 출력

    stack.pop();        // 황희 pop

    stack.display();    // [전체 학생의 수 =  2] 출력
    return 0;
}

